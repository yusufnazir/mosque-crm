package com.mosque.crm.enums;

/**
 * Types of mosque memberships.
 */
public enum MembershipType {
    /**
     * Full member with voting rights
     */
    FULL,

    /**
     * Associate member without voting rights
     */
    ASSOCIATE,

    /**
     * Youth member (under 18)
     */
    YOUTH,

    /**
     * Senior member (65+)
     */
    SENIOR,

    /**
     * Family membership
     */
    FAMILY
}
