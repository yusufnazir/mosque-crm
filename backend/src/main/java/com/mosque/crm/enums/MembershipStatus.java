package com.mosque.crm.enums;

/**
 * Status of a membership record.
 */
public enum MembershipStatus {
    /**
     * Currently active membership
     */
    ACTIVE,

    /**
     * Expired membership
     */
    EXPIRED,

    /**
     * Suspended membership
     */
    SUSPENDED,

    /**
     * Cancelled membership
     */
    CANCELLED
}
